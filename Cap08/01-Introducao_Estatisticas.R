#' As três grandes Áreas da Estatísticas
#' 
#' Probabilidade – estudo da "aleatoriedade" e da "incerteza".
#' - Utiliza métodos de quantificação das chances associadas aos diversos resultados.   
#' 
#' Estatística Descritiva – utiliza métodos para "coleta", "organização", "apresentação", "análise" e "síntese" de dados
#' obtidos em uma população ou amostra.
#' 
#' Estatística Inferencial - é o processo de ESTIMAR informações sobre uma "população" a partir dos resultados observados
#' em um "amostra".
#' 
#' 
#' 
#' Estatística é a ciência, parte da Matemática Aplicada, que fornece métodos para coletar, descrever, analisar,
#' apresentar e interpretar dados, para a utilização dos mesmos na tomada de decisões.
#' 
#' 
#' 
#' População x Amostra
#' 
#' População - São TODOS os elementos distintos – indivíduos, itens ou objetos – cujas características estejam sendo estudadas.
#' 
#' Amostra - É UMA PARTE da população, sendo coletada a partir da população que está sendo estudada.
#' 
#' 
#' A melhor estratégia para criar uma amostra fiel é através de "randomização" ou "aleatorização".
#' 
#' 
#' Parâmetros x Estatísticas
#' 
#' As estatísticas se baseiam nos dados da amostra e não em dados populacionais. 
#' Quando se coletam dados de toda uma população, temos o chamado censo.
#' 
#' Se você depois resume toda a informação do censo em um número, esse número é um parâmetro, não uma estatística.
#' 
#' Parâmetros - características sobre a "população". 
#' Valores calculados usando dados da "população" são chamados de "parâmetros".
#' 
#' Estatísticas - características sobre a "amostra". 
#' Valores calculados usando dados da "amostra" são chamados de "estatísticas".
#'
#' 
#'  
#'   "Amostra" é usado para CALCULAR "Estatísticas" (% da amostra prefere a marca de sapatos X)
#'   "Estatísticas" é usado para ESTIMAR "Parâmetros"
#'   "Parâmetros "é usado para COMPREENDER a "População" (% da população prefere a marca de sapatos X)
#'    
#'     
#' Observações x Variáveis
#' 
#' Dado != Informação
#' 
#' Dados - valores coletados através de observação ou medição.
#' 
#' Informação - dados que são transformados em fatos relevantes e usados para um propósito específico.
#'
#'
#' Dados não fazem sentido, se não forem colocados em um contexto.       
#' 
#' Observação - Uma observação é uma ocorrência de um item de dados específico que é gravada sobre uma unidade de dados. Também
#' chamada de registro.
#' 
#' Variável - Variável é a característica de interesse que é medida em cada elemento da amostra ou população. Como o nome sugere, seus
#' valores variam de elemento para elemento. As variáveis podem ter valores numéricos ou não numéricos.
#'         
#'          
#' Tipos de Variáveis
#' 
#' Qualitativas -  utilizam termos descritivos para descrever algo de interesse. Ex: cor dos olhos, estado civil, religião, sexo, grau de
#' escolaridade, classe social, tipo sanguíneo, cor da pele, etc...  
#' 
#'        Podem ser Nominas(Sem Ordem/hierarquia) ou Ordinais(Possuem Ordem/hierarquia)       
#'            
#'             
